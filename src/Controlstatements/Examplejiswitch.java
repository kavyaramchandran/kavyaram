package Controlstatements;

public class Examplejiswitch {

	public static void main(String[] args) {
	int a=55;
	switch(a)
	{
	case 0-24:System.out.println("The grade is F");
	break;
	case 25-45:System.out.println("The grade is E");
	break;
	case 46-50:System.out.println("The grade is D");
	break;
	case 51-60:System.out.println("The grade is C");
	break;
	case 62-80:System.out.println("The grade is B");
	break;
	case 81-100:System.out.println("The grade is A");
	break;
	default:System.out.println("invalid expression");
	}

	}

}
