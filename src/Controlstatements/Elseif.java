public class Elseif {

	public static void main(String[] args) {
		 
		int a=25,b=64,c=12;
		if(a>b && a>c)
		{
			System.out.println("a is greatest number");
		}
		else if(b>a && b>c)
		{
			System.out.println("b is greatest number");
		}
		else 
		{
            System.out.println("c is greatest number");
	}
	}
}
