package Controlstatements;

public class examplejitnesfor {

	public static void main(String[] args) {
		for(int i=1;i<=3;i++)
		{
			for(int j=1;j<=3;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		for(int n=1;n<=4;n++)
		{
			for(int m=1;m<=n;m++)
			{
				System.out.print("* ");
			}
			System.out.println();
		}

		}
		

	}


